package level1;

public class 짝수와홀수 {

	public static void main(String[] args) {
		/*
		 * 문제 설명 
		 * 정수 num이 짝수일 경우 
		 * "Even"을 반환하고 홀수인 경우 "Odd"를 반환하는 함수, 
		 * solution을 완성해주세요.
		 * 
		 * 제한 조건 
		 * num은 int 범위의 정수입니다. 0은 짝수입니다
		 * 
		 */
		System.out.println(solution(-11));
		
	}

	public static String solution(int num) {
		
		System.out.println(num%2+","+num/2);
		if(num == 0) {
			return "Even";
		}
		
		if(num%2 == 1 || num%2 == -1 ) {
			return "Odd";
		}
		
		return "Even";
	}
	

}

// 다른 사람 풀이
class 짝수와홀수_다른사람 {
    public String solution(int num) {
        return num%2 == 0 ? new String("Even") : new String("Odd");
    }
}